package com;

public class Customer {
private int cid;
private String cname;
private String age;
public int getCid() {
	return cid;
}
public void setCid(int cid) {
	
	this.cid = cid;
}
public String getCname() {
	return cname;
}
public void setCname(String cname) {
	this.cname = cname;
}
public String getAge() {
	return age;
}
public void setAge(String age) {
	this.age = age;
}

}
